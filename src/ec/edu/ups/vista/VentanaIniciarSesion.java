    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtCorreo.getText().equalsIgnoreCase("") && txtContrasena.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Rellene todas las casillas", "Error", 0);
        } else {
            String correo = txtCorreo.getText();
            String contrasena = txtContrasena.getText();
            if (ventanaRegistrar.controladorUsuario().iniciarSesion(correo, contrasena) != null) {
                JOptionPane.showMessageDialog(null, "Sesion iniciada");  
            } else {
                JOptionPane.showMessageDialog(null, "No existe la cuenta");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

