    public boolean validarCedula(String cedula){
        if(cedula.length() != 10){
            return false;
        }for (int i = 0; i < cedula.length(); i++) {
            if(!(cedula.charAt(i) == '0' || cedula.charAt(i) == '1' || cedula.charAt(i) == '2' || cedula.charAt(i) == '3' || cedula.charAt(i) == '4' || cedula.charAt(i) == '5' || cedula.charAt(i) == '6' || cedula.charAt(i) == '7' || cedula.charAt(i) == '8' || cedula.charAt(i) == '9')){
                return false;
            }
        }
        
        int[] ci = new int[10];
        int[] mult = new int[9];
        
        for (int i = 0; i < cedula.length(); i++) {
            String digito = cedula.charAt(i) + "";
            int numero = Integer.valueOf(digito);
            ci[i] = numero;
        }
        
        int multi;
        
        for(int i = 0; i < ci.length - 1; i++){
            if(i % 2 == 0){
                multi = ci[i] * 2;
                if(multi > 9){
                    mult[i] = multi - 9;
                } else {
                    mult[i] = multi;
                }
            } else {
                mult[i] = ci[i];
            }
        }
        
        int suma = 0;
        
        for (int i = 0; i < mult.length; i++) {
            suma += mult[i];
        }
        
        int sumafin =suma + ci[9];
        if(sumafin % 10 == 0){
            return true;
        } else {
            return false;
        }
    }

